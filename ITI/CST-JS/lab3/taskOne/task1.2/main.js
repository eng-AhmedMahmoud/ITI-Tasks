// ask user to enter circle radius
var radius = prompt("Enter circle radius");
// calculate circle area
var area = Math.PI * radius * radius;
// print circle area
console.log("Circle area: " + area);


// ask user to enter value to calculate square root
var value = prompt("Enter value to calculate square root");
// calculate square root
var sqrt = Math.sqrt(value);
// print square root
console.log("Square root: " + sqrt);


// ask user to enter value to calculate angle cosine
var value = prompt("Enter value to calculate angle cosine");
// calculate angle cosine
var cos = Math.cos(value);
// print angle cosine
console.log("Angle cosine: " + cos);
